package com.maulana.latihan.praktikum2;

public class Buku {
    String judul, namaPengarang;
    int tahunTerbit, jmlHalaman, harga;

    Buku(String judul, String namaPengarang, int tahunTerbit, int jmlHalaman, int harga) {
        this.judul = judul;
        this.namaPengarang = namaPengarang;
        this.tahunTerbit = tahunTerbit;
        this.jmlHalaman = jmlHalaman;
        this.harga = harga;
    }
}
