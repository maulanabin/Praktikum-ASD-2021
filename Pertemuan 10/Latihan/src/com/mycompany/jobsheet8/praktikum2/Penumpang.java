public class Penumpang {
    public String nama, kotaAsal, kotaTujuan;
    public int jumlahTiket, harga;

    public Penumpang(String nama, String kotaAsal, String kotaTujuan, int jumlahTiket, int harga) {
        this.nama = nama;
        this.kotaAsal = kotaAsal;
        this.kotaTujuan = kotaTujuan;
        this.jumlahTiket = jumlahTiket;
        this.harga = harga;
    }
}
