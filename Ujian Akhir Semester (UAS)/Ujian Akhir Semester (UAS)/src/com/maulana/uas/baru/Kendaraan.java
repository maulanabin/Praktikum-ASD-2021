package com.maulana.uas.baru;

public class Kendaraan {
    String tnkb;
    String nama;
    String jenis;
    String cc;
    String tahun;
    String bulanBayar;

    public Kendaraan(String tnkb, String nama, String jenis, String cc, String tahun, String bulanBayar) {
        this.tnkb = tnkb;
        this.nama = nama;
        this.jenis = jenis;
        this.cc = cc;
        this.tahun = tahun;
        this.bulanBayar = bulanBayar;

    }
}

